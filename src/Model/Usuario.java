package Model;

public class Usuario {

    String nome;
    String email;
    String endereco;
    String telContato;

    public String getNome() {
        return nome;
    }

    public String getEmail() {
        return email;
    }

    public String getEndereco() {
        return endereco;
    }

    public String getTelefone() {
        return telContato;
    }

}
